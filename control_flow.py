#Control Flow
#Control flow allows you to direct the execution path of your code based on conditions or repeated actions.

# Conditionals (if, elif, else)
# Conditionals help your program make decisions.
age = 20
if age < 18:
    print("you are minor") # If age is less than 18, this block runs.
elif age < 65:
    print("you are adult") # If age is between 18 and 64, this block runs.
else:
    print("you are senior") # If age is 65 or older, this block runs.