# Loops
# Loops are used to execute a block of code repeatedly.
# print(1)
# print(2)
# print(3)
# print(4)
# The above code prints numbers 1 to 4, but we can do this more efficiently


# using a loop.
# A loop is a block of code that runs repeatedly until a certain condition is met.

# For example
for i in range(5):
    print(i) # This will print numbers from 0 to 4.

#While Loop:
#Repeats code as long as a condition is true.
count = 0
while count < 5: # This block of code will run as long as count is less than 5.
    print(count)  # This will print numbers from 0 to 4.
    count += 1  # Increment count by 1 each time the loop runs.