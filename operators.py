#Arithmetic Operators:
#Used for mathematical calculations.
x = 10
y = 3
print("the total is: ", x + y)  # Output: 13
print("the difference is: ", x - y)  # Output: 7
print("the product is: ", x * y)  # Output: 30
print("the quotient is: ", x / y)  # Output: 3.333
print("the floor division is: ", x // y)  # Output: 3
print("the modulus is: ", x % y)  # Output: 1


# Comparison Operators:
# Used to compare two values and return a boolean (True or False).

# Checks if x is greater than y; prints True.
print(x > y)

# Checks if x is less than y; prints False.
print(x < y)

# Checks if x is equal to y; prints False.
print(x == y)

#Logical Operators:
#Combine boolean values.
a = True
b = False
print(a and b)  # Output: False (both must be True)
print(a or b)   # Output: True (at least one must be True)
print(not a)    # Output: False (inverts the value of a)