# Integer: a whole number without a decimal point.
a = 5 
print(a)  # This will output: 5 


# Float: a number that has a decimal point.
pi = 3.14 
print(pi)  # This will output: 3.14 


# String: a sequence of characters (text).
name = "Noor"  
print(name)  # This will output: Alice


# Boolean: represents True or False.
is_valid = True  
print(is_valid)  # This will output: True